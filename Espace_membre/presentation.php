			<h4>Pourquoi Numeric Native, une alternative pour l'alternance ?</h4>
			<p>Numeric Native est une vitrine visant à partager une expérience de recrutement différente.<br />
			Dans cette optique Numeric Native propose une expression aux candidats et aux recruteurs, un échange + des projets = Collaboration.<br />
			Un calcul simpliste mais offrant une flexibilité nouvelle à portée de clic !</p>

			<p>Numeric Native est initiative de deux collaborateurs souhaitant obtenir une visibilité pour un recrutement en études supérieures.<br />
			Auto-didactes, nos deux accolites prirent en main leur apprentissage des langages de dévelopemment web (HTML 5, CSS3, PHP) et SGBDR (MySQL) et mîrent en place cette plate-forme.<br />
			Une plate-forme figurant sur leur documents de candidature, permettant aux recruteurs d'en apprendre plus sur leur candidat et leurs compétences.<br />
			Plus qu'une vitrine, Numeric Native est une plate-forme d'échange où les utilisateurs, candidats et recruteurs peuvent commenter leur expérience avec le projet.</p>

			<p>Le candidat poste son projet via le formulaire de son espace utilisateur, une fois le projet posté, celui-ci sera repertorié dans la liste des projets et pourra être visualisé par les recruteurs.<br />
			Le projet doit être utilisable, par exemple, le projet doit être un programme, site-web, ou tout autre expérience.<br />
			Un retour d'expérience pourra être fait par les autres utilisateurs, ce qui permet de pouvoir améliorer l'expérience proposée.</p>
